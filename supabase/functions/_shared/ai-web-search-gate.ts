export function shouldSkipWebSearchForMenuItem(itemHint: string): boolean {
  const normalized = itemHint.toLowerCase().replace(/[^a-z0-9+&\s-]/g, " ");
  const tokens = normalized.split(/[\s+&/-]+/).filter(Boolean);
  if (tokens.length === 0) return true;
  const commonFoodTokens = new Set([
    "bagel",
    "coffee",
    "latte",
    "muffin",
    "donut",
    "doughnut",
    "sandwich",
    "pizza",
    "taco",
    "pastry",
    "tea",
    "smoothie",
    "cookie",
    "burger",
    "fries",
    "salad",
    "croissant",
    "espresso",
    "cappuccino",
    "cortado",
    "matcha",
    "cold",
    "brew",
    "iced",
    "cake",
    "cupcake",
    "brownie",
    "burrito",
    "wrap",
    "soup",
    "bread",
    "toast",
    "lemonade",
    "juice",
    "boba",
    "chai",
    "americano",
    "mocha",
    "scone",
    // Widely known items an LLM already recognizes — no paid lookup needed.
    "quesadilla",
    "empanada",
    "churro",
    "nachos",
    "torta",
    "tamale",
    "tamales",
    "elote",
    "tostada",
    "enchilada",
    "gelato",
    "affogato",
    "frappe",
    "frappuccino",
    "macchiato",
    "pretzel",
    "waffle",
    "pancake",
    "milkshake",
    "shake",
    "gyro",
    "falafel",
    "hummus",
    "ramen",
    "pho",
    "sushi",
    "dumpling",
    "noodle",
    "noodles",
    "curry",
    "kebab",
    "bibimbap",
    "kimbap",
    "mochi",
    "panini",
    "quiche",
    "biscuit",
    "danish",
    "parfait",
    "bowl",
    "roll",
    "pie",
    "rice",
  ]);
  const connectorTokens = new Set(["and", "with", "plus", "free", "bogo", "buy", "one", "get", "a", "an", "the"]);
  return tokens.every((token) => commonFoodTokens.has(token) || connectorTokens.has(token));
}
